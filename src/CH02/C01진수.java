package CH02;

public class C01진수 {

	public static void main(String[] args) {
		
		
		
		System.out.printf("10 진수 : %d\n", 0b00001111);
		System.out.printf("10 진수 : %d\n", 173); // 10 진수
		System.out.printf("10 진수 : %d\n", 0255);// 8진수  (0 : 8진수를 의미하는 접두사)
		System.out.printf("10 진수 : %d\n", 0xAD);// 16진수 (0x : 16진수 를 의미하는 접두사)

		System.out.printf("8 진수 : %o\n", 173); // 10 진수
		System.out.printf("8 진수 : %o\n", 0255);// 8진수	  (0 : 8진수를 의미하는 접두사)	
		System.out.printf("8 진수 : %o\n", 0xAD);// 16진수  (0x : 16진수 를 의미하는 접두사)
		
		System.out.printf("16 진수 : %x\n", 173); //10 진수
		System.out.printf("16 진수 : %x\n", 0255);// 8진수  (0 : 8진수를 의미하는 접두사)
		System.out.printf("16 진수 : %x\n", 0xAD);// 16진수 (0x : 16진수 를 의미하는 접두사)
		
		
	}

}
